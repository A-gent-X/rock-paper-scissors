class Lizard:
    def __init__(self):
        self.name = ""
        self.weakness = ""
        self.attack_type = ""

    def set_name(self):
        self.name = "Reptile"
        print(self.name)

    def set_weakness(self):
        self.weakness = "Boulder Barrage"
        print(self.weakness)

    def set_attack_type(self):
        self.attack_type = "Komodo Acid, Poison"
        print(self.attack_type)